    private String currOp = "+";
    private Double currValue = Double.valueOf(0);
    private boolean canClearLabel = true; //Придумать человеческое название!!!

    private class NumListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            if (resLabel.getText().length() > 12) {
                return;
            }

            if (canClearLabel) {                   //Жёсткие костыли
                resLabel.setText("");
                canClearLabel = false;
            }

            JButton currButton = (JButton) e.getSource();

            if ("0".equals(resLabel.getText())) {
                resLabel.setText("");
            }

            String result = resLabel.getText() + currButton.getText();
            resLabel.setText(result);
        }
    }

    private class OpListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            canClearLabel = true;

            Double token = Double.parseDouble(resLabel.getText());
            resLabel.setText(currValue.toString());
            //cleverPrint(currValue.toString());

            JButton currButton = (JButton) e.getSource();

            switch (currOp) {
                case "+":
                    currValue += token;
                    break;
                case "-":
                    currValue -= token;
                    break;
                case "*":
                    currValue *= token;
                    break;
                case "/":
                    currValue /= token;
                    break;
                case "=":
                    currValue = token;
                    break;
            }
            currOp = currButton.getText();

            if ("sqrt".equals(currButton.getText())) {
                currValue = Math.sqrt(token);
            }
            
            if ("±".equals(currButton.getText())) {
                currValue *= -1;
            }

            resLabel.setText(currValue.toString());
        }
    }

    private class ClearListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            JButton currButton = (JButton) e.getSource();

            resLabel.setText("0");
            if ("C".equals(currButton.getText())) {
                currValue = Double.valueOf(0);
                currOp = "+";
                canClearLabel = true;
            }
        }
    }

